window.hashString = (str) => {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash);
  }
  return hash;
};

window.seededRandom = (seed) => {
  const x = Math.sin(seed) * 10000;
  return x - Math.floor(x);
};

window.getCardPriceHistory = (card, timeline) => {
  const pts = [];
  const seedStr = `${card.id}_${card.parallel || 'Base'}_${card.sport}`;
  const seed = window.hashString(seedStr);
  const r1 = window.seededRandom(seed);
  const r2 = window.seededRandom(seed + 1);
  const r3 = window.seededRandom(seed + 2);

  const f1 = 2 * Math.PI / 7;        
  const f2 = 2 * Math.PI / 30;       
  const f3 = 2 * Math.PI / 360;      
  const f4 = 2 * Math.PI / 1;        
  const f5 = 2 * Math.PI / (1 / 24); 

  const amp1 = 0.015 * (0.5 + r1);
  const amp2 = 0.035 * (0.5 + r2);
  const amp3 = 0.075 * (0.5 + r3);
  const amp4 = 0.006 * (0.5 + r1);
  const amp5 = 0.002 * (0.5 + r2);

  const getPriceAtDaysAgo = (daysAgo) => {
    const pct = (card.pctChange || 0) / 100;
    const basePrice = card.value * Math.pow(1 + pct, -daysAgo / 360);
    
    const w1 = Math.sin(daysAgo * f1) * amp1;
    const w2 = Math.sin(daysAgo * f2) * amp2;
    const w3 = Math.sin(daysAgo * f3) * amp3;
    const w4 = Math.sin(daysAgo * f4) * amp4;
    const w5 = Math.sin(daysAgo * f5) * amp5;
    const wLong = Math.sin(daysAgo * (2 * Math.PI / 1800)) * 0.12 * r3;

    const totalNoise = w1 + w2 + w3 + w4 + w5 + wLong;
    const finalPrice = basePrice * (1 + totalNoise);
    
    return Math.max(5, Math.round(finalPrice));
  };

  const now = new Date();
  
  if (timeline === '1D') {
    for (let i = 0; i < 7; i++) {
      const hoursAgo = 24 - i * 4;
      const d = new Date(now.getTime() - hoursAgo * 60 * 60 * 1000);
      const daysAgo = hoursAgo / 24;
      const price = i === 6 ? card.value : getPriceAtDaysAgo(daysAgo);
      const dateStr = i === 6 ? 'Now' : d.toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' });
      pts.push({ date: dateStr, price });
    }
  } else if (timeline === '1M') {
    for (let i = 0; i < 7; i++) {
      const daysAgo = 30 - i * 5;
      const d = new Date(now.getTime() - daysAgo * 24 * 60 * 60 * 1000);
      const price = i === 6 ? card.value : getPriceAtDaysAgo(daysAgo);
      const dateStr = i === 6 ? 'Today' : d.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
      pts.push({ date: dateStr, price });
    }
  } else if (timeline === '6M') {
    for (let i = 0; i < 7; i++) {
      const daysAgo = 180 - i * 30;
      const d = new Date(now.getTime() - daysAgo * 24 * 60 * 60 * 1000);
      const price = i === 6 ? card.value : getPriceAtDaysAgo(daysAgo);
      const dateStr = i === 6 ? 'Today' : d.toLocaleDateString('en-US', { month: 'short' });
      pts.push({ date: dateStr, price });
    }
  } else if (timeline === '1Y') {
    for (let i = 0; i < 7; i++) {
      const daysAgo = 360 - i * 60;
      const d = new Date(now.getTime() - daysAgo * 24 * 60 * 60 * 1000);
      const price = i === 6 ? card.value : getPriceAtDaysAgo(daysAgo);
      const dateStr = i === 6 ? 'Today' : d.toLocaleDateString('en-US', { month: 'short', year: '2-digit' });
      pts.push({ date: dateStr, price });
    }
  } else if (timeline === '5Y') {
    for (let i = 0; i < 7; i++) {
      const daysAgo = 1800 - i * 300;
      const d = new Date(now.getTime() - daysAgo * 24 * 60 * 60 * 1000);
      const price = i === 6 ? card.value : getPriceAtDaysAgo(daysAgo);
      const dateStr = i === 6 ? 'Today' : d.toLocaleDateString('en-US', { year: 'numeric' });
      pts.push({ date: dateStr, price });
    }
  }
  
  return pts;
};

