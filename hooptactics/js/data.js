window.getCardRarityPriority = (card) => {
  if (!card) return 5;
  const type = (card.type || '').toLowerCase();
  const rarity = (card.rarity || '').toLowerCase();
  const parallel = (card.parallel || '').toLowerCase();
  const id = (card.id || '').toLowerCase();
  
  const isAuto = type.includes('auto') || parallel.includes('auto') || id.includes('-auto');
  const isPatch = type.includes('patch') || parallel.includes('patch') || id.includes('-patch') || parallel.includes('swatch') || id.includes('-swatch');
  const isAutoOrPatch = isAuto || isPatch;

  const isGoldenRare = rarity === 'one-of-one' || rarity === 'gold' || rarity === 'prismatic' || rarity === 'cosmic' || rarity === 'green' || rarity === 'pink' ||
                      parallel.includes('one-of-one') || parallel.includes('gold') || parallel.includes('prismatic') || parallel.includes('cosmic') || parallel.includes('green') || parallel.includes('pink');

  if (isAutoOrPatch && isGoldenRare) {
    return 1; // Golden/Rare Autographs & Patches
  }
  if (isAutoOrPatch) {
    return 2; // Other Autographs & Patches (e.g. base/silver autos/patches)
  }
  if (isGoldenRare) {
    return 3; // Other Golden/Rare cards (e.g. Jordan PMG Red/Green, Carmelo 1/1, etc.)
  }
  const isParallel = parallel.includes('refractor') || parallel.includes('prizm') || parallel.includes('parallel') || parallel.includes('gems') || parallel.includes('limited') || (card.rarity && card.rarity !== 'base');
  if (isParallel) {
    return 4; // Mid tier parallels (silver, refractors, limited, gems, etc.)
  }
  return 5; // Standard base cards
};
